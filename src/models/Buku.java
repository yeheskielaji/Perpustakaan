public class Buku {
    private String Kode_buku;
    private String Judul;
    private String Pengarang;
    private Integer Jml_buku;
    private String Kode_Penerbit;

    public String getKode_buku() {
        return Kode_buku;
    }

    public void setKode_buku(String Kode_buku) {
        this.Kode_buku = Kode_buku;
    }

    public String getJudul() {
        return Judul;
    }

    public void setJudul(String Judul) {
        this.Judul = Judul;
    }

    public String getPengarang() {
        return Pengarang;
    }

    public void setPengarang(String Pengarang) {
        this.Pengarang = Pengarang;
    }

    public Integer getJml_buku() {
        return Jml_buku;
    }

    public void setJml_buku(Integer Jml_buku) {
        this.Jml_buku = Jml_buku;
    }

    public String getKode_Penerbit() {
        return Kode_Penerbit;
    }

    public void setKode_Penerbit(String Kode_Penerbit) {
        this.Kode_Penerbit = Kode_Penerbit;
    }
    
    
}
